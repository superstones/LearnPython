favorite_language=' Python '
favorite_language=favorite_language.rstrip()#要永久删除空白，必须将删除操作的结果存回变量中,rstrip为去掉末尾的空白
print(favorite_language)
favorite_language_2=' Python '
favorite_language_2=favorite_language_2.lstrip()#lstrip为去掉字符串左边的空白
print(favorite_language_2)
favorite_language_2=favorite_language.strip()#strip为去掉字符串两端的空白
print(favorite_language_2)

