age = 25
if age < 2:
    print("He is a baby.")
elif age > 2 and age <= 4:
    print("He is a toddler.")
elif age >= 4 and age < 13:
    print("He is a child.")
elif age >= 13 and age < 20:
    print("He is a teenager")
elif age >= 20 and age < 65:
    print("He is a adult")
else:
    print("He is a old people")
